    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String Professoor = jComboBox1.getSelectedItem().toString();
      

        Connection con = Conexao.AbrirConexao();
        ProfessorDAO sql = new ProfessorDAO(con);
        Professor a = new Professor();

        if (Professoor.equals("")) {

            JOptionPane.showMessageDialog(null, "Nenhum nome selecionado", "BQGP", JOptionPane.WARNING_MESSAGE);

        }
        else {

            int b = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir" + "\n ("+ Professoor +")" , "BQGP", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (b == 0) {
                
                a.setNome(Professoor);
                sql.Excluir_Professor(a);
                Conexao.FecharConexao(con);
                dispose();
                JOptionPane.showMessageDialog(null,"Categoria Professor excluido com sucesso");
                new Menu().setVisible(true);

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

