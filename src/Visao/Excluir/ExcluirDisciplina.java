    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String Disciplinaa = jComboBox1.getSelectedItem().toString();
        

        Connection con = Conexao.AbrirConexao();
        DisciplinaDAO sql = new DisciplinaDAO(con);
        Disciplina a = new Disciplina();

        if (Disciplinaa.equals("")) {

            JOptionPane.showMessageDialog(null, "Nenhum nome selecionado", "BQGP", JOptionPane.WARNING_MESSAGE);

        }
        else {

            int b = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir" + "\n ("+ Disciplinaa +") ", "BQGP", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (b == 0) {
                
               
                a.setNome(Disciplinaa);
                sql.Excluir_Disciplina(a);
                Conexao.FecharConexao(con);
                dispose();
                JOptionPane.showMessageDialog(null,"Categoria Disciplina excluida com sucesso");
                new Menu().setVisible(true);

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

