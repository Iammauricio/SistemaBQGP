    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
        String Disciplinaa = jComboBox1.getSelectedItem().toString();
        String Assuntoo  = jComboBox2.getSelectedItem().toString();

        Connection con = Conexao.AbrirConexao();
        AssuntoDAO sql = new AssuntoDAO(con);
        Assunto a = new Assunto();

        if (Disciplinaa.equals("")) {

            JOptionPane.showMessageDialog(null, "Nenhum nome selecionado", "BQGP", JOptionPane.WARNING_MESSAGE);

        }
        else {

            int b = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir" + "\n ("+ Disciplinaa +") ("+ Assuntoo +") ", "BQGP", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (b == 0) {
                
                a.setAssunto(Assuntoo);
                a.setDisciplina(Disciplinaa);
                sql.Excluir_Assunto(a);
                Conexao.FecharConexao(con);
                dispose();
               
                new Menu().setVisible(true);
                dispose();
            }

        }

        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

