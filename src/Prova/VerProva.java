public void Atualizar_Tabela(String valor){
        String nome = valor;
         Connection con = Conexao.AbrirConexao();
         QuestoesDAO bd = new  QuestoesDAO(con);

          List< Questoes> lista = new ArrayList<>();
          lista  =  bd.Duplo(nome);
 
          DefaultTableModel tbm = (DefaultTableModel) tabela.getModel();

          while(tbm.getRowCount() > 0){
          tbm.removeRow(0);
          }
          int i = 0;
          for( Questoes tab : lista){
          tbm.addRow(new String[i]);
         tabela.setValueAt(i+1+")", i,0);
         tabela.setValueAt(tab.getAssunto(), i,1);
         tabela.setValueAt(tab.getQuestao(), i,2);
         tabela.setValueAt(tab.getA(), i,3);
         tabela.setValueAt(tab.getB(), i,4);
         tabela.setValueAt(tab.getC(), i,5);
         tabela.setValueAt(tab.getD(), i,6);
         tabela.setValueAt(tab.getE(), i,7);
          i++;
          }
          Conexao.FecharConexao(con);

}



